package Operators;

public class ArithmaticOperator {
    public static void main(String[] args) {
        int x=100;
        int y=20;

        System.out.println(x+y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(x++);
        System.out.println(x--);
    }
}
