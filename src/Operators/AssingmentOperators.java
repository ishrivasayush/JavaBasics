package Operators;

public class AssingmentOperators {
    public static void main(String[] args) {
        int a=100;
        System.out.println(a=200);
        System.out.println(a-=200);
        System.out.println(a+=200);   //a=a+200
        System.out.println(a*=200);
        System.out.println(a%=200);
        System.out.println(a/=200);
    }
}
